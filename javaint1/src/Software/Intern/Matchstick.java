package Software.Intern;

public class Matchstick {
    public Matchstick() {
        System.out.println("No-args constructor of Matchstick");
    }

    public void ignite() {
        System.out.println("Striking the matchstick to produce a flame");
    }

    public void burn() {
        System.out.println("The matchstick burns for a few seconds");
    }

    public void extinguish() {
        System.out.println("Extinguishing the matchstick after use");
    }

    public void dispose() {
        System.out.println("Disposing of the used matchstick safely");
    }
}
