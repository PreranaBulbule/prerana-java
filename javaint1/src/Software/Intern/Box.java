package Software.Intern;

public class Box {
    public Box() {
        System.out.println("No-args constructor of Box");
    }

    public void open() {
        System.out.println("Opening the box");
    }

    public void store() {
        System.out.println("Storing items inside the box");
    }

    public void close() {
        System.out.println("Closing the box securely");
    }

    public void move() {
        System.out.println("Moving the box to a safe place");
    }
}

