package Software.Intern;

public class Bangle {
    public Bangle() {
        System.out.println("No-args constructor of Bangle");
    }

    public void wear() {
        System.out.println("Wearing the bangle");
    }

    public void clean() {
        System.out.println("Cleaning the bangle with a soft cloth");
    }

    public void remove() {
        System.out.println("Removing the bangle carefully");
    }

    public void store() {
        System.out.println("Storing the bangle in a jewelry box");
    }
}

