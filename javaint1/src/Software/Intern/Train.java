package Software.Intern;

public class Train {
    public Train() {
        System.out.println("No-args constructor of Train");
    }

    public void startTrain() {
        System.out.println("Starting the train");
    }

    public void runTrain() {
        System.out.println("Train is running on tracks");
    }

    public void stopTrain() {
        System.out.println("Stopping the train at the station");
    }

    public void parkTrain() {
        System.out.println("Parking the train at the depot");
    }
}
