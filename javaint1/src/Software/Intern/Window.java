package Software.Intern;

public class Window {
    public Window() {
        System.out.println("No-args constructor of Window");
    }

    public void open() {
        System.out.println("Opening the window");
    }

    public void close() {
        System.out.println("Closing the window");
    }

    public void clean() {
        System.out.println("Cleaning the window with a cloth");
    }

    public void lock() {
        System.out.println("Locking the window for security");
    }
}

