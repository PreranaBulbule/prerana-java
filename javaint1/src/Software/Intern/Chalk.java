package Software.Intern;

public class Chalk {
    public Chalk() {
        System.out.println("No-args constructor of Chalk");
    }

    public void write() {
        System.out.println("Writing on the board using chalk");
    }

    public void breakChalk() {
        System.out.println("Chalk breaking into smaller pieces");
    }

    public void dustOff() {
        System.out.println("Dusting off the chalk powder from hands");
    }

    public void store() {
        System.out.println("Storing the chalk in a box to prevent breakage");
    }
}

