package Software.Intern;

public class Sharpener {
    public Sharpener() {
        System.out.println("No-args constructor of Sharpener");
    }

    public void sharpen() {
        System.out.println("Sharpening the pencil to get a fine tip");
    }

    public void hold() {
        System.out.println("Holding the sharpener while sharpening");
    }

    public void clean() {
        System.out.println("Cleaning the sharpener to remove wood shavings");
    }

    public void store() {
        System.out.println("Storing the sharpener in a pencil case");
    }
}

