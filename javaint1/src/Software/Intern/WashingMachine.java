package Software.Intern;

public class WashingMachine {
    public WashingMachine() {
        System.out.println("No-args constructor of WashingMachine");
    }

    public void startMachine() {
        System.out.println("Starting the washing machine");
    }

    public void washClothes() {
        System.out.println("Washing clothes in the machine");
    }

    public void rinseClothes() {
        System.out.println("Rinsing clothes after washing");
    }

    public void stopMachine() {
        System.out.println("Stopping the washing machine");
    }
}
