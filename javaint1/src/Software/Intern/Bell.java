package Software.Intern;

public class Bell {
    public Bell() {
        System.out.println("No-args constructor of Bell");
    }

    public void ring() {
        System.out.println("Ringing the bell to produce sound");
    }

    public void stop() {
        System.out.println("Stopping the bell from ringing");
    }

    public void clean() {
        System.out.println("Cleaning the bell to maintain its shine");
    }

    public void store() {
        System.out.println("Storing the bell in a safe place");
    }
}

