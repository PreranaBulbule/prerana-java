package Software.Intern;

public class Rope {
    public Rope() {
        System.out.println("No-args constructor of Rope");
    }

    public void tie() {
        System.out.println("Tying the rope securely");
    }

    public void untie() {
        System.out.println("Untying the rope carefully");
    }

    public void coil() {
        System.out.println("Coiling the rope for storage");
    }

    public void store() {
        System.out.println("Storing the rope in a dry place to prevent damage");
    }
}
