package Software.Intern;

public class Ladder {
    public Ladder() {
        System.out.println("No-args constructor of Ladder");
    }

    public void climb() {
        System.out.println("Climbing the ladder carefully");
    }

    public void stand() {
        System.out.println("Standing on the ladder to reach high places");
    }

    public void fold() {
        System.out.println("Folding the ladder for easy storage");
    }

    public void store() {
        System.out.println("Storing the ladder in a safe place");
    }
}
