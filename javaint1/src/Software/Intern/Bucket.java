package Software.Intern;

public class Bucket {
    public Bucket() {
        System.out.println("No-args constructor of Bucket");
    }

    public void fillBucket() {
        System.out.println("Filling the bucket with water");
    }

    public void emptyBucket() {
        System.out.println("Emptying the bucket");
    }

    public void cleanBucket() {
        System.out.println("Cleaning the bucket");
    }

    public void storeBucket() {
        System.out.println("Storing the bucket in a dry place");
    }
}

