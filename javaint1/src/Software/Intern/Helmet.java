public class Helmet {
    public Helmet() {
        System.out.println("No-args constructor of Helmet");
    }

    public void wear() {
        System.out.println("Wearing the helmet for protection");
    }

    public void adjust() {
        System.out.println("Adjusting the helmet straps for a secure fit");
    }

    public void remove() {
        System.out.println("Removing the helmet carefully");
    }

    public void store() {
        System.out.println("Storing the helmet in a safe place");
    }
}

