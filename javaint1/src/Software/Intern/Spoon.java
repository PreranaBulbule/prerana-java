package Software.Intern;

public class Spoon {
    public Spoon() {
        System.out.println("No-args constructor of Spoon");
    }

    public void scoop() {
        System.out.println("Scooping food with the spoon");
    }

    public void stir() {
        System.out.println("Stirring ingredients using the spoon");
    }

    public void serve() {
        System.out.println("Using the spoon to serve food");
    }

    public void clean() {
        System.out.println("Cleaning the spoon after use");
    }
}
