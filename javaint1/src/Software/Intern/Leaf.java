package Software.Intern;

public class Leaf {
    public Leaf() {
        System.out.println("No-args constructor of Leaf");
    }

    public void photosynthesize() {
        System.out.println("The leaf is performing photosynthesis");
    }

    public void changeColor() {
        System.out.println("The leaf changes color in different seasons");
    }

    public void fall() {
        System.out.println("The leaf falls from the tree in autumn");
    }

    public void store() {
        System.out.println("Preserving the leaf in a book");
    }
}
