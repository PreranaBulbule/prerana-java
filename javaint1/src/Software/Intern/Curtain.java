package Software.Intern;

public class Curtain {
    public Curtain() {
        System.out.println("No-args constructor of Curtain");
    }

    public void open() {
        System.out.println("Opening the curtain to let light in");
    }

    public void close() {
        System.out.println("Closing the curtain for privacy");
    }

    public void wash() {
        System.out.println("Washing the curtain to keep it clean");
    }

    public void store() {
        System.out.println("Storing the curtain properly when not in use");
    }
}
