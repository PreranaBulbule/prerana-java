package Software.Intern;

public class Mouse {
    public Mouse() {
        System.out.println("No-args constructor of Mouse");
    }

    public void click() {
        System.out.println("Clicking the mouse");
    }

    public void scroll() {
        System.out.println("Scrolling using the mouse wheel");
    }

    public void move() {
        System.out.println("Moving the mouse cursor");
    }

    public void connect() {
        System.out.println("Connecting the mouse to the computer");
    }
}
