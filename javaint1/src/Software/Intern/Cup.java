package Software.Intern;

public class Cup {
    public Cup() {
        System.out.println("No-args constructor of Cup");
    }

    public void fill() {
        System.out.println("Filling the cup with liquid");
    }

    public void drink() {
        System.out.println("Drinking from the cup");
    }

    public void wash() {
        System.out.println("Washing the cup after use");
    }

    public void store() {
        System.out.println("Storing the cup in the cupboard");
    }
}
