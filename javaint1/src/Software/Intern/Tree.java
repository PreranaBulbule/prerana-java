package Software.Intern;

public class Tree {
    public Tree() {
        System.out.println("No-args constructor of Tree");
    }

    public void grow() {
        System.out.println("The tree is growing");
    }

    public void shedLeaves() {
        System.out.println("The tree is shedding leaves in autumn");
    }

    public void provideShade() {
        System.out.println("The tree provides shade");
    }

    public void bearFruit() {
        System.out.println("Some trees bear fruit in the right season");
    }
}
