package Software.Intern;

public class Napkin {
    public Napkin() {
        System.out.println("No-args constructor of Napkin");
    }

    public void use() {
        System.out.println("Using the napkin to wipe hands or face");
    }

    public void fold() {
        System.out.println("Folding the napkin neatly after use");
    }

    public void dispose() {
        System.out.println("Disposing of the napkin properly");
    }

    public void store() {
        System.out.println("Storing the napkin in a clean place");
    }
}
