package Software.Intern;

public class Fan {
    public Fan() {
        System.out.println("No-args constructor of Fan");
    }

    public void switchOn() {
        System.out.println("Turning on the fan");
    }

    public void adjustSpeed() {
        System.out.println("Adjusting the fan speed");
    }

    public void rotate() {
        System.out.println("Fan blades are rotating");
    }

    public void switchOff() {
        System.out.println("Turning off the fan");
    }
}
