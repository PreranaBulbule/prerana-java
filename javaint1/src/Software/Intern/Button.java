package Software.Intern;

public class Button {
    public Button() {
        System.out.println("No-args constructor of Button");
    }

    public void fasten() {
        System.out.println("Fastening the button to secure clothing");
    }

    public void unfasten() {
        System.out.println("Unfastening the button to loosen clothing");
    }

    public void clean() {
        System.out.println("Cleaning the button to maintain its appearance");
    }

    public void store() {
        System.out.println("Storing extra buttons in a safe place");
    }
}

