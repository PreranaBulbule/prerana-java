package Software.Intern;

public class Candle {
    public Candle() {
        System.out.println("No-args constructor of Candle");
    }

    public void light() {
        System.out.println("Lighting the candle to produce light");
    }

    public void melt() {
        System.out.println("Candle wax melting as it burns");
    }

    public void extinguish() {
        System.out.println("Blowing out the candle to stop the flame");
    }

    public void store() {
        System.out.println("Storing the candle in a cool, dry place");
    }
}
