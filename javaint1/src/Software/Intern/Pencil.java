package Software.Intern;

public class Pencil {
    public Pencil() {
        System.out.println("No-args constructor of Pencil");
    }

    public void write() {
        System.out.println("Writing with the pencil");
    }

    public void sharpen() {
        System.out.println("Sharpening the pencil to maintain a fine tip");
    }

    public void erase() {
        System.out.println("Erasing the pencil marks");
    }

    public void store() {
        System.out.println("Storing the pencil in a pencil case");
    }
}
