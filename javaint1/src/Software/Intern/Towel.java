package Software.Intern;

public class Towel {
    public Towel() {
        System.out.println("No-args constructor of Towel");
    }

    public void use() {
        System.out.println("Using the towel to dry off");
    }

    public void wash() {
        System.out.println("Washing the towel to keep it clean");
    }

    public void dry() {
        System.out.println("Drying the towel in the sun");
    }

    public void store() {
        System.out.println("Storing the towel in a dry place");
    }
}

