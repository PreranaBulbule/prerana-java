package Software.Intern;

public class Battery {
    public Battery() {
        System.out.println("No-args constructor of Battery");
    }

    public void providePower() {
        System.out.println("Providing power to electronic devices");
    }

    public void deplete() {
        System.out.println("Battery charge depleting over time");
    }

    public void dispose() {
        System.out.println("Disposing of the used battery properly");
    }

    public void store() {
        System.out.println("Storing the battery in a cool, dry place");
    }
}
