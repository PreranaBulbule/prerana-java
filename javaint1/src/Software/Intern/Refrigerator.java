package Software.Intern;

public class Refrigerator {
    public Refrigerator() {
        System.out.println("No-args constructor of Refrigerator");
    }

    public void cool() {
        System.out.println("Cooling the stored food items");
    }

    public void freeze() {
        System.out.println("Freezing items in the freezer compartment");
    }

    public void store() {
        System.out.println("Storing perishable food safely");
    }

    public void defrost() {
        System.out.println("Defrosting the freezer to remove ice buildup");
    }
}
