package Software.Intern;

public class Ball {
    public Ball() {
        System.out.println("No-args constructor of Ball");
    }

    public void bounce() {
        System.out.println("The ball is bouncing");
    }

    public void roll() {
        System.out.println("The ball is rolling on the ground");
    }

    public void throwBall() {
        System.out.println("Throwing the ball");
    }

    public void catchBall() {
        System.out.println("Catching the ball");
    }
}
