package Software.Intern;

public class Hat {
    public Hat() {
        System.out.println("No-args constructor of Hat");
    }

    public void wear() {
        System.out.println("Wearing the hat to protect from sun or cold");
    }

    public void adjust() {
        System.out.println("Adjusting the hat for a comfortable fit");
    }

    public void remove() {
        System.out.println("Removing the hat carefully");
    }

    public void store() {
        System.out.println("Storing the hat in a safe place");
    }
}
