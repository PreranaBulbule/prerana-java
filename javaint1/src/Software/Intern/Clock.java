package Software.Intern;

public class Clock {
    public Clock() {
        System.out.println("No-args constructor of Clock");
    }

    public void showTime() {
        System.out.println("Displaying the current time");
    }

    public void setAlarm() {
        System.out.println("Setting an alarm on the clock");
    }

    public void tick() {
        System.out.println("The clock is ticking");
    }

    public void stop() {
        System.out.println("Stopping the clock mechanism");
    }
}
