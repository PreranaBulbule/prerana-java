package Software.Intern;

public class Bottle {
    public Bottle() {
        System.out.println("No-args constructor of Bottle");
    }

    public void fillBottle() {
        System.out.println("Filling the bottle with liquid");
    }

    public void drinkFromBottle() {
        System.out.println("Drinking from the bottle");
    }

    public void cleanBottle() {
        System.out.println("Cleaning the bottle");
    }

    public void closeBottle() {
        System.out.println("Closing the bottle with a cap");
    }
}

