package Software.Intern;

public class Bus {
    public Bus() {
        System.out.println("No-args constructor of Bus");
    }

    public void startBus() {
        System.out.println("Starting the bus");
    }

    public void driveBus() {
        System.out.println("Driving the bus");
    }

    public void stopBus() {
        System.out.println("Stopping the bus");
    }

    public void parkBus() {
        System.out.println("Parking the bus at the terminal");
    }
}

