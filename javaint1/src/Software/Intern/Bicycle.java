package Software.Intern;

public class Bicycle {
    public Bicycle() {
        System.out.println("No-args constructor of Bicycle");
    }

    public void start() {
        System.out.println("Starting to ride the bicycle");
    }

    public void pedal() {
        System.out.println("Pedaling the bicycle to move forward");
    }

    public void brake() {
        System.out.println("Applying brakes to slow down the bicycle");
    }

    public void stop() {
        System.out.println("Stopping the bicycle");
    }
}
