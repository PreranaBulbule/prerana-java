package Software.Intern;

public class Slippers {
    public Slippers() {
        System.out.println("No-args constructor of Slippers");
    }

    public void wear() {
        System.out.println("Wearing the slippers for comfort");
    }

    public void remove() {
        System.out.println("Removing the slippers before entering the house");
    }

    public void clean() {
        System.out.println("Cleaning the slippers to remove dust");
    }

    public void store() {
        System.out.println("Storing the slippers in the shoe rack");
    }
}
