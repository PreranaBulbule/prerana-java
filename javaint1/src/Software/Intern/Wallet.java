package Software.Intern;

public class Wallet {
    public Wallet() {
        System.out.println("No-args constructor of Wallet");
    }

    public void open() {
        System.out.println("Opening the wallet to access cash and cards");
    }

    public void close() {
        System.out.println("Closing the wallet securely");
    }

    public void storeMoney() {
        System.out.println("Storing money and cards in the wallet");
    }

    public void keepSafe() {
        System.out.println("Keeping the wallet in a secure place");
    }
}

