package Software.Intern;

public class Mango {
    public Mango() {
        System.out.println("No-args constructor of Mango");
    }

    public void grow() {
        System.out.println("The mango is growing on the tree");
    }

    public void ripen() {
        System.out.println("The mango is ripening under the sun");
    }

    public void pluck() {
        System.out.println("The mango is being plucked from the tree");
    }

    public void eat() {
        System.out.println("Eating the delicious mango");
    }
}
