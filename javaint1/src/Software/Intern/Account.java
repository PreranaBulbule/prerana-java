package Software.Intern;

public class Account {
    public Account() {
        System.out.println("No-args constructor of Account");
    }

    public void open() {
        System.out.println("Opening a new account");
    }

    public void deposit() {
        System.out.println("Depositing money into the account");
    }

    public void withdraw() {
        System.out.println("Withdrawing money from the account");
    }

    public void close() {
        System.out.println("Closing the account");
    }
}
