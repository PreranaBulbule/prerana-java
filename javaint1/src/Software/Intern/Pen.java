package Software.Intern;

public class Pen {
    public Pen() {
        System.out.println("No-args constructor of Pen");
    }

    public void write() {
        System.out.println("Writing with the pen");
    }

    public void refill() {
        System.out.println("Refilling the pen");
    }

    public void capOn() {
        System.out.println("Putting the cap on the pen");
    }

    public void capOff() {
        System.out.println("Removing the cap from the pen");
    }
}

