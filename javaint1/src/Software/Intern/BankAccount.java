package Software.Intern;

public class BankAccount {
    public BankAccount() {
        System.out.println("No-args constructor of BankAccount");
    }

    public void deposit() {
        System.out.println("Depositing money into the account");
    }

    public void withdraw() {
        System.out.println("Withdrawing money from the account");
    }

    public void checkBalance() {
        System.out.println("Checking account balance");
    }

    public void closeAccount() {
        System.out.println("Closing the bank account");
    }
}
