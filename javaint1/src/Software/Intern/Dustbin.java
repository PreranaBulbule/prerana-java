package Software.Intern;

public class Dustbin {
    public Dustbin() {
        System.out.println("No-args constructor of Dustbin");
    }

    public void openLid() {
        System.out.println("Opening the dustbin lid");
    }

    public void throwWaste() {
        System.out.println("Throwing waste into the dustbin");
    }

    public void closeLid() {
        System.out.println("Closing the dustbin lid");
    }

    public void emptyDustbin() {
        System.out.println("Emptying the dustbin when full");
    }
}
