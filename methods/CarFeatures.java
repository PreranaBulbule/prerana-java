class CarFeatures {
    public void enableTurbo() {
        System.out.println("Car Features: Turbo mode activated!");
    }

    public void activateABS() {
        System.out.println("Car Features: Anti-lock Braking System (ABS) engaged.");
    }

    public void applyLaunchControl() {
        System.out.println("Car Features: Launch control system is enabled for a fast start.");
    }

    public void adjustAerodynamics() {
        System.out.println("Car Features: Active aerodynamics adjusted for performance.");
    }

    public void displayFuelEfficiency() {
        System.out.println("Car Features: Displaying fuel efficiency details...");
    }
}
